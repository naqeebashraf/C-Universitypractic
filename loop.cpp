#include<iostream>
using namespace std;
int main()
{
int n;
cout<<"enter the number";
cin>>n;
for(int i=0;n<=11;i++)
{
	if(n==8)
	{
	cout<<"eight"<<endl;	
	}
	else if(n==9)
	{
		cout<<"nine"<<endl;
	}
	else if(n>9==0)
	{
		cout<<"it is even number"<<endl;
	}
	else if(n>9!=0)
	{
		cout<<"it is odd";
	}
}
return 0;
	
}

